package cgbox.vo;

public class TheaterVO {
	
	private int theater_no;
	private int mtheater_no;
	private int theater_seat;
	private String theater_kind;
	private int theater_price;
	private int theater_name;
	
	
	public int getTheater_no() {
		return theater_no;
	}
	public void setTheater_no(int theater_no) {
		this.theater_no = theater_no;
	}
	public int getMtheater_no() {
		return mtheater_no;
	}
	public void setMtheater_no(int mtheater_no) {
		this.mtheater_no = mtheater_no;
	}
	public int getTheater_seat() {
		return theater_seat;
	}
	public void setTheater_seat(int theater_seat) {
		this.theater_seat = theater_seat;
	}
	public String getTheater_kind() {
		return theater_kind;
	}
	public void setTheater_kind(String theater_kind) {
		this.theater_kind = theater_kind;
	}
	public int getTheater_price() {
		return theater_price;
	}
	public void setTheater_price(int theater_price) {
		this.theater_price = theater_price;
	}
	public int getTheater_name() {
		return theater_name;
	}
	public void setTheater_name(int theater_name) {
		this.theater_name = theater_name;
	}
	
	
	
}
